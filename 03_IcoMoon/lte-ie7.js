window.onload = function() {
	function addIcon(el, entity) {
		var html = el.innerHTML;
		el.innerHTML = '<span style="font-family: \'ico_arrow\'">' + entity + '</span>' + html;
	}
	var icons = {
			'ico-arrow_bottom' : '&#x2193;',
			'ico-arrow_left' : '&#x2190;',
			'ico-arrow_right' : '&#x2192;',
			'ico-arrow_top' : '&#x2191;'
		},
		els = document.getElementsByTagName('*'),
		i, attr, html, c, el;
	for (i = 0; ; i += 1) {
		el = els[i];
		if(!el) {
			break;
		}
		attr = el.getAttribute('data-icon');
		if (attr) {
			addIcon(el, attr);
		}
		c = el.className;
		c = c.match(/ico-[^\s'"]+/);
		if (c && icons[c[0]]) {
			addIcon(el, icons[c[0]]);
		}
	}
};